// Primitive Data Types

// Strings: Textual data enclosesd "" or ''
let firstName = "John";
console.log("First Name:",firstName);

// Number: Represents a numeric value (30,-30,3.14)
let age = 30;
console.log(age);
console.log("The data type of age is: ", typeof age);
console.log("The data type of age is: ", typeof firstName);

// Boolean: either true or false
let isAdmin = true;
console.log("Is admin: ",isAdmin);
console.log("The data type of isAdmin is: ", typeof isAdmin);

//Null: Intentional abscense of a value
let car = null;
console.log("car: " ,car)
console.log("The data type of Car is: ", typeof car);

// Undefined: Unintentional abscense of a value
let City;
console.log("City: ", City);
